package ejerciciosDoWhile;

public class Ejercicio1 {
	public static void main(String[] args) {
		// creamos las variables a usar
		int contador = 0;

		// creamos bucle con do while poniendo primero las instrucciones que se van a
		// llevar a cabo a continuación.
		do {

			System.out.println(contador);
			contador++;
		} while (contador <= 20);
	}
	
}
